    public void data_load() {

        // load customer
        try {

            Statement s = db.mycon().createStatement();

            ResultSet rs = s.executeQuery("SELECT * FROM customer");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString("customer_name"));

                DefaultComboBoxModel com = new DefaultComboBoxModel(v);
                com_cust.setModel(com);

            }

        } catch (SQLException e) {
            System.out.println(e);
        }

        // load Product
        try {

            Statement s = db.mycon().createStatement();

            ResultSet rs = s.executeQuery("SELECT * FROM product");
            Vector v = new Vector();

            while (rs.next()) {
                v.add(rs.getString("Product_Name"));

                DefaultComboBoxModel com = new DefaultComboBoxModel(v);
                com_pro.setModel(com);

            }

        } catch (SQLException e) {
            System.out.println(e);
        }

        // load last invoice number
        try {

            Statement s = db.mycon().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM extra WHERE exid =1");

            if (rs.next()) {

                inid.setText(rs.getString("val"));

            }

        } catch (SQLException e) {
            System.out.println(e);
        }

        // pluss new invoice
        int i = Integer.valueOf(inid.getText());
        i++;
        inid.setText(String.valueOf(i));

    }

    public void pro_tot_cal() {

        // product calculation
        Double qt = Double.valueOf(p_qty.getText());
        Double price = Double.valueOf(u_price.getText());
        Double tot;

        tot = qt * price;

        tot_price.setText(String.valueOf(tot));

    }

    public void cart_total() {

        int numofrow = sale_table.getRowCount();

        double total = 0;

        for (int i = 0; i < numofrow; i++) {

            double value = Double.valueOf(sale_table.getValueAt(i, 5).toString());
            total += value;
        }
        bill_tot.setText(Double.toString(total));

        /// total qty count 
        int numofrows = sale_table.getRowCount();

        double totals = 0;

        for (int i = 0; i < numofrows; i++) {

            double values = Double.valueOf(sale_table.getValueAt(i, 3).toString());
            totals += values;
        }
        tot_qty.setText(Double.toString(totals));

    }

    public void tot() {

        Double paid = Double.valueOf(paid_amt.getText());
        Double tot = Double.valueOf(bill_tot.getText());

        Double discField = Double.valueOf(discFieldMain.getText());

        Double due;
        if (disc_amt.getText().equals("0")) {
            due = paid - tot;
        } else {
            due = paid - discField;
        }

        discFielddd.setText(disc_amt.getText());

        balance.setText(String.valueOf(due));

    }

