    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try {

            // `cartid`, `INID`, `Product_Name`, `Bar_code`, `qty`, `Unit_Price`, `Total_Price`
            DefaultTableModel dt = (DefaultTableModel) sale_table.getModel();
            int rc = dt.getRowCount();

            for (int i = 0; i < rc; i++) {

                String inid = dt.getValueAt(i, 0).toString(); // get inid
                String P_name = dt.getValueAt(i, 1).toString(); // get product name
                String bar_code = dt.getValueAt(i, 2).toString(); // get barcode
                String qty = dt.getValueAt(i, 3).toString(); // get product qty
                String un_price = dt.getValueAt(i, 4).toString(); // get product unit price
                String tot_price = dt.getValueAt(i, 5).toString(); // get product total Price

                // cart DB
                Statement s = db.mycon().createStatement();
                s.executeUpdate(" INSERT INTO cart (INID, Product_Name, Bar_code, qty, Unit_Price, Total_Price, qty1) VALUES ('" + inid + "','" + P_name + "','" + bar_code + "','" + qty + "','" + un_price + "','" + tot_price + "', '" + qty + "') ");

                Statement s2 = db.mycon().createStatement();
                s2.executeUpdate("UPDATE product set qty=(qty  - '" + qty + "') where product_Name = '" + P_name + "'");

            }

            JOptionPane.showMessageDialog(null, "Data Seved");

        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
        }

        try {

            // sales DB
            //`saleid`, `INID`, `Cid`, `Customer_Name`, `Total_Qty`, `Total_Bill`, `Status`, `Balance`
            String inv_id = inid.getText();
            String cname = com_cust.getSelectedItem().toString();
            String totqty = tot_qty.getText();
            String tot_bil = bill_tot.getText();
            String blnc = balance.getText();
            String bill_after_disc = discFieldMain.getText();

            // paid check
            Double tot = Double.valueOf(discFieldMain.getText());
            Double pid = Double.valueOf(paid_amt.getText());
            String discount_amt = disc_amt.getText();
            String Status = null;
            if (pid.equals(0.0)) {

                Status = "UnPaid";

            } else if (tot > pid) {
                Status = "Partial";

            } else if (tot <= pid) {
                Status = "Paid";
            }

            if (discFieldMain.getText().equals("0")) {
                Statement ss = db.mycon().createStatement();
                ss.executeUpdate("INSERT INTO sales(INID, Cid, Customer_Name, Total_Qty, Total_Bill, Status, Balance, tot_bill_after_disc, disc_amt) VALUES('" + inv_id + "','" + cus_id + "','" + cname + "','" + totqty + "','" + tot_bil + "','" + Status + "','" + blnc + "', '" + tot_bil + "', '"+discount_amt+"')");
            } else {
                Statement ss = db.mycon().createStatement();
                ss.executeUpdate("INSERT INTO sales(INID, Cid, Customer_Name, Total_Qty, Total_Bill, Status, Balance, tot_bill_after_disc, disc_amt) VALUES('" + inv_id + "','" + cus_id + "','" + cname + "','" + totqty + "','" + tot_bil + "','" + Status + "','" + blnc + "', '" + bill_after_disc + "', '"+discount_amt+"')");
            }

        } catch (NumberFormatException | SQLException e) {
            System.out.println(e);
        }

        try {

            String id = inid.getText();
            Statement s = db.mycon().createStatement();
            s.executeUpdate("UPDATE  extra SET val='" + id + "' WHERE exid = 1");

        } catch (SQLException e) {
            System.out.println(e);
        }

        // Print or view ireport bill
        try {

            HashMap para = new HashMap();

            para.put("inv_id", inid.getText());  // inv_id  is ireport parameter name

            ReportView r = new ReportView("src\\reports\\reportPrint.jasper", para);
            r.setVisible(true);

        } catch (Exception e) {
        }

        DefaultTableModel dt = (DefaultTableModel) sale_table.getModel();
        dt.setRowCount(0);
        cart_total();
        tot();
        data_load();

    }//GEN-LAST:event_jButton4ActionPerformed

